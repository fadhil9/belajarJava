public class switchTest {
    switchTest(int nilai){
        switch(nilai){
            case 90:
                System.out.println("A");
                break;
            case 70:
                System.out.println("B");
                break;
            case 60:
                System.out.println("C");
                break;
            case 50:
                System.out.println("D");
                break;
            default:
                System.out.println("Nilai Belum Daftar");
        }
    }
}
